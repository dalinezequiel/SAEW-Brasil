package com.sae.model;

public class ComboBoxModel {
	private int idComboBox;;
	private String comboBoxItem;
	private String nomeComboBox;

	public ComboBoxModel() {
		super();
	}

	public int getIdComboBox() {
		return idComboBox;
	}

	public void setIdComboBox(int idComboBox) {
		this.idComboBox = idComboBox;
	}

	public String getComboBoxItem() {
		return comboBoxItem;
	}

	public void setComboBoxItem(String comboBoxItem) {
		this.comboBoxItem = comboBoxItem;
	}

	public String getNomeComboBox() {
		return nomeComboBox;
	}

	public void setNomeComboBox(String nomeComboBox) {
		this.nomeComboBox = nomeComboBox;
	}
}
