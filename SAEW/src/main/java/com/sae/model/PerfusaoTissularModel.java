package com.sae.model;

public class PerfusaoTissularModel {
	private int idPerfusaoTissular;
	private String perfusaoTissular;
	private String nomeModel;
	
	public PerfusaoTissularModel() {
		super();
	}

	public int getIdPerfusaoTissular() {
		return idPerfusaoTissular;
	}
	public void setIdPerfusaoTissular(int idPerfusaoTissular) {
		this.idPerfusaoTissular = idPerfusaoTissular;
	}
	public String getPerfusaoTissular() {
		return perfusaoTissular;
	}
	public void setPerfusaoTissular(String perfusaoTissular) {
		this.perfusaoTissular = perfusaoTissular;
	}
	public String getNomeModel() {
		return nomeModel;
	}
	public void setNomeModel(String nomeModel) {
		this.nomeModel = nomeModel;
	}
}
